    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (!modelBook.validData(txtBookID.getText().trim(), txtBookName.getText().trim(), txtAuthor.getText().trim(),
                txtPublisher.getText().trim())) {
            return;
        }
        if (addNewBook) {
            try {
                if (!modelBook.validDatabookID(txtBookID.getText().trim())) {
                    JOptionPane.showMessageDialog(this, "DUPLICATED BOOKID at " + txtBookID.getText().trim());
                    return;
                }
                Books_SE140736 b = new Books_SE140736(txtBookID.getText().toUpperCase().trim(),
                        txtBookName.getText().trim(),
                        txtAuthor.getText().trim(),
                        txtPublisher.getText().trim(),
                        Integer.parseInt(cbxPublishedYear.getSelectedItem().toString()),
                        chkForRent.isSelected());
                if (modelBook.Insert(b) == SUCCESS) {
                    modelBook.getModelBooks().getList().add(b);
                }
                addNewBook = false;
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else {
            try {
                int selectedRow = tblBook.getSelectedRow();
                if (selectedRow < 0) {
                    JOptionPane.showMessageDialog(this, "Please Choose Row to Edit");
                }

                Books_SE140736 bOld = modelBook.getModelBooks().getList().get(selectedRow);
                Books_SE140736 bNew = new Books_SE140736(txtBookID.getText().trim().toUpperCase(),
                        txtBookName.getText().trim(),
                        txtAuthor.getText().trim(),
                        txtPublisher.getText().trim(),
                        Integer.parseInt(cbxPublishedYear.getSelectedItem().toString()),
                        chkForRent.isSelected());
                if (modelBook.Save(bNew) == SUCCESS) {
                    bOld.setBookName(bNew.getBookName());
                    bOld.setAuthor(bNew.getAuthor());
                    bOld.setPublisher(bNew.getPublisher());
                    bOld.setPublishedYear(bNew.getPublishedYear());
                    bOld.setForRent(bNew.isForRent());
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        tblBook.updateUI();
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        try {
            int selectedRow = tblBook.getSelectedRow();
            if (selectedRow < 0 || deleteBook == false) {
                JOptionPane.showMessageDialog(this, "Choose the Row to Delete");
                return;
            }
            int r = JOptionPane.showConfirmDialog(this, "Do you want to Delete this Book?");
            if (r == JOptionPane.YES_OPTION) {
                Books_SE140736 b = modelBook.getModelBooks().getList().get(selectedRow);
                if (modelBook.Delete(b) == SUCCESS) {
                    deleteBook = false;
                    modelBook.getModelBooks().getList().remove(b);
                    JOptionPane.showMessageDialog(this, "Deleted");
                    clearInformationBookOnTable();
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        tblBook.updateUI();
    }//GEN-LAST:event_btnRemoveActionPerformed

    private void btnFindIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindIDActionPerformed
        addNewBook = false;
        String bookID = txtBookID.getText();
        try {
            modelBook.loadData();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } catch (Exception ex) {
            Logger.getLogger(ManagementUI_SE140736.class.getName()).log(Level.SEVERE, null, ex);
        }
        for (int i = 0; i < modelBook.getModelBooks().getList().size(); i++) {
            if (bookID.equals(modelBook.getModelBooks().getList().get(i).getBookID())) {
                clearMouseClickedBook();

                modelBook = new tblBooks_SE140736();
                tblBook.setModel(modelBook.getModelBooks());

                Books_SE140736 book = modelBook.getModelBooks().getList().get(i);
                displayDetailBook(book);
                tblBook.setRowSelectionInterval(i, i); //focus dong dc chon
                return;
            }
        }
        JOptionPane.showMessageDialog(null, "Không tìm thấy ID này!");
    }//GEN-LAST:event_btnFindIDActionPerformed

