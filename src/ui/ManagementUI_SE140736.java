    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String bookName = txtSearchByName.getText();
        if (bookName.length() == 0) {
            JOptionPane.showMessageDialog(null, "Search by name phải khác rỗng!");
            return;
        }
        for (int i = 0; i < modelBook.getModelBooks().getList().size(); i++) {
            if (modelBook.getModelBooks().getList().get(i).getBookName().contains(bookName) == true) {
                try {
                    modelBook = new tblBooks_SE140736();
                    modelBook.loadTableBookSearchByName(bookName);
                    tblBook.setModel(modelBook.getModelBooks());
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, ex.getMessage());
                }
                return;
            }
        }
        JOptionPane.showMessageDialog(null, "Không tìm thấy tên sách này!");
    }//GEN-LAST:event_btnSearchActionPerformed

